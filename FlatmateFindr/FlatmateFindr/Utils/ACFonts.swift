import SwiftUI

extension Font {
    static func montserratBold(size: CGFloat) -> Font {
        Font.custom("Montserrat-Bold", size: size)
    }
    static func montserratMedium(size: CGFloat) -> Font {
        Font.custom("Montserrat-Medium", size: size)
    }
    static func montserratRegular(size: CGFloat) -> Font {
        Font.custom("Montserrat-Regular", size: size)
    }
    static func montserratSemiBold(size: CGFloat) -> Font {
        Font.custom("Montserrat-SemiBold", size: size)
    }
    static func montserratItalic(size: CGFloat) -> Font {
        Font.custom("Montserrat-Italic", size: size)
    }
    static func interBold(size: CGFloat) -> Font {
        Font.custom("Inter-Bold", size: size)
    }
    static func interBlack(size: CGFloat) -> Font {
        Font.custom("Inter-Black", size: size)
    }
    static func interRegular(size: CGFloat) -> Font {
        Font.custom("Inter-Regular", size: size)
    }
    static func interMedium(size: CGFloat) -> Font {
        Font.custom("Inter-Medium", size: size)
    }
    static func interSemiBold(size: CGFloat) -> Font {
        Font.custom("Inter-SemiBold", size: size)
    }
}
